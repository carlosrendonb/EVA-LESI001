                    <p>El cifrado simétrico utiliza la misma clave para cifrar y descifrar los datos. Algunos ejemplos de cifrado simétrico son DES (roto) y AES. Estos algoritmos tienden a ser más rápidos que la criptografía asimétrica y utilizan claves más pequeñas (las claves de 128 o 256 bits son comunes para AES, las claves DES tienen una longitud de 56 bits).</p> 
