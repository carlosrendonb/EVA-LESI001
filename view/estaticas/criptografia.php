              <p>GnuPG o GPG es una implementación de código abierto de PGP del proyecto GNU. Es posible que deba utilizar GPG para descifrar archivos en CTF. Con PGP / GPG, las claves privadas se pueden proteger con frases de contraseña de manera similar a las claves privadas SSH. Si la clave está protegida con contraseña, puede intentar descifrar esta contraseña usando John The Ripper y gpg2john. La clave proporcionada en esta tarea no está protegida con una frase de contraseña.</p>
