                <h3>¿Qué es el intercambio de claves?</h3>
              <p>El intercambio de claves permite que 2 personas / partes establezcan un conjunto de claves criptográficas comunes sin que un observador pueda obtener estas claves. Generalmente, para establecer claves simétricas comunes.</p> 
              <h3>¿Cómo funciona el intercambio de claves Diffie Hellman?</h3>
              <p>Alice y Bob quieren hablar de forma segura. Quieren establecer una clave común, para poder usar criptografía simétrica, pero no quieren usar el intercambio de claves con criptografía asimétrica. Aquí es donde entra en juego DH Key Exchange.</p>
              <p>Alice y Bob tienen secretos que generan, llamémoslos A y B.También tienen algún material común que es público, llamémoslo C.</p>
              <p>Necesitamos hacer algunas suposiciones. En primer lugar, siempre que combinamos secretos / material es imposible o muy muy difícil separarlos. En segundo lugar, el orden en el que se combinan no importa.</p>
              <p>Alice y Bob combinarán sus secretos con el material común y formarán AC y BC. Luego se enviarán estos entre sí y combinarán eso con sus secretos para formar dos claves idénticas, ambas ABC. Ahora pueden usar esta clave para comunicarse.</p>
              <h3>Recursos extra</h3>
              <p>Un excelente video si desea una explicación visual está disponible aquí. <a href="https://www.youtube.com/watch?v=NmM9HA2MQGI">https://www.youtube.com/watch?v=NmM9HA2MQGI</a></p>
              <p>El intercambio de claves DH se usa a menudo junto con la criptografía de clave pública RSA, para demostrar la identidad de la persona con la que está hablando con la firma digital. Esto evita que alguien ataque la conexión con un ataque de intermediario pretendiendo ser Bob.</p>
