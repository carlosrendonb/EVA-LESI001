              <p>Las computadoras cuánticas pronto serán un problema para muchos tipos de cifrado.</p> 
              <h3>Asimétrico y cuántico</h3>
              <p>Si bien es poco probable que tengamos computadoras cuánticas lo suficientemente potentes hasta alrededor de 2030, una vez que existan, el cifrado que utiliza RSA o criptografía de curva elíptica será muy rápido de romper. Esto se debe a que las computadoras cuánticas pueden resolver de manera muy eficiente los problemas matemáticos en los que se basan estos algoritmos para su fuerza.</p>
              <h3>AES / DES y Quantum</h3>
              <p>También es probable que AES con claves de 128 bits sea roto por computadoras cuánticas en un futuro cercano, pero AES de 256 bits no se puede romper tan fácilmente. Triple DES también es vulnerable a los ataques de computadoras cuánticas.</p>
              <h3>Recomendaciones actuales</h3>
              <p>La NSA recomienda utilizar RSA-3072 o mejor para el cifrado asimétrico y AES-256 o mejor para el cifrado simétrico. Actualmente se están ejecutando varios concursos para algoritmos criptográficos cuánticos seguros, y es probable que tengamos un nuevo estándar de cifrado antes de que las computadoras cuánticas se conviertan en una amenaza para RSA y AES.</p>
              <h3>Obtenga más información sobre las computadoras cuánticas y la criptografía</h3>
              <p>Si desea obtener más información sobre esto, NIST tiene recursos que detallan cuáles son los problemas con el cifrado actual y las soluciones propuestas actualmente para estos. <a href="https://doi.org/10.6028/NIST.IR.8105">https://doi.org/10.6028/NIST.IR.8105</a></p>
