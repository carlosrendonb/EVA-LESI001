                  <p style="padding: 0px 20px;">El cifrado asimétrico utiliza un par de claves, una para cifrar y la otra en el par para descifrar. Algunos ejemplos son RSA y criptografía de curva elíptica. Normalmente, estas claves se denominan clave pública y clave privada. Los datos cifrados con la clave privada se pueden descifrar con la clave pública y viceversa. Su clave privada debe mantenerse privada, de ahí el nombre. El cifrado asimétrico tiende a ser más lento y utiliza claves más grandes, por ejemplo, RSA normalmente utiliza claves de 2048 a 4096 bits.
