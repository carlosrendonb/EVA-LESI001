              <p>AES, a veces llamado Rijndael en honor a sus creadores, significa Advanced Encryption Standard. Fue un reemplazo para DES que tenía claves cortas y otros defectos criptográficos.</p>
              <p>Tanto AES como DES operan en bloques de datos (un bloque es una serie de bits de tamaño fijo).</p>
