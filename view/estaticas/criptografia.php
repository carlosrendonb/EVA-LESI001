                <h3>Cifrado y autenticación SSH</h3>
              <p>De forma predeterminada, SSH se autentica con nombres de usuario y contraseñas de la misma manera que iniciaría sesión en la máquina física.</p> 
              <p>En algún momento, es casi seguro que acceda a una máquina que tiene SSH configurado con autenticación de clave. Esto utiliza claves públicas y privadas para demostrar que el cliente es un usuario válido y autorizado en el servidor. De forma predeterminada, las claves SSH son claves RSA. Puede elegir qué algoritmo generar y / o agregar una frase de contraseña para cifrar la clave SSH. <a style="background-color: black; color:white; padding: 0px 10px;">ssh-keygenes</a> el programa utilizado para generar pares de claves la mayor parte del tiempo.</p>
              <h3>Claves privadas SSH</h3>
              <p>Debe tratar sus claves SSH privadas como contraseñas. No las comparta, se llaman claves privadas por una razón. Si alguien tiene su clave privada, puede usarla para iniciar sesión en servidores que la aceptarán a menos que la clave esté encriptada.</p>
              <p>Es muy importante mencionar que la frase de contraseña para descifrar la clave no se usa para identificarte en el servidor, todo lo que hace es descifrar la clave SSH. La frase de contraseña nunca se transmite y nunca sale de su sistema.</p>
              <p>Con herramientas como John the Ripper, puede atacar una clave SSH cifrada para intentar encontrar la frase de contraseña, lo que resalta la importancia de usar una frase de contraseña segura y mantener la privacidad de su clave privada.</p>
              <p>Al generar una clave SSH para iniciar sesión en una máquina remota, debe generar las claves en su máquina y luego copiar la clave pública, ya que esto significa que la clave privada nunca existe en la máquina de destino. Para las claves temporales generadas para acceder a las cajas CTF, esto no importa tanto.</p>
              <h3>¿Cómo utilizo estas claves?</h3>
              <p>La carpeta ~ / .ssh es el lugar predeterminado para almacenar estas claves para OpenSSH. El <a style="background-color: black;color:white;padding: 0px 10px;">authorized_keys</a> archivo (tenga en cuenta la ortografía en inglés de EE. UU.) de este directorio contiene claves públicas que pueden acceder al servidor si la autenticación de claves está habilitada. De forma predeterminada, en muchas distribuciones, la autenticación de clave está habilitada, ya que es más seguro que usar una contraseña para autenticarse. Normalmente, para el usuario root, solo se habilita la autenticación de clave.</p>
              <p>Para utilizar una clave SSH privada, los permisos deben configurarse correctamente; de ​​lo contrario, su cliente SSH ignorará el archivo con una advertencia. Solo el propietario debe poder leer o escribir en la clave privada (600 o más estricta). <a style="background-color: black;color:white;padding:0px 10px;">ssh -i keyNameGoesHere user@host</a> es cómo se especifica una clave para el cliente Linux OpenSSH estándar.</p>
              <h3>Usando claves SSH para obtener un mejor shell</h3>
              <p>Las claves SSH son una excelente manera de "actualizar" un shell inverso, asumiendo que el usuario tiene habilitado el inicio de sesión (www-data normalmente no lo tiene, pero los usuarios regulares y root sí). Dejar una clave SSH en allowed_keys en una caja puede ser una puerta trasera útil, y no necesita lidiar con ninguno de los problemas de las carcasas inversas no estabilizadas como Control-C o la falta de finalización de la pestaña.</p>
