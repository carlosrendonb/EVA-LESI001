                <h3>¿Qué es una firma digital?</h3>
              <p>Las firmas digitales son una forma de demostrar la autenticidad de los archivos, de demostrar quién los creó o modificó. Al utilizar la criptografía asimétrica, produce una firma con su clave privada y puede verificarse utilizando su clave pública. Como solo usted debe tener acceso a su clave privada, esto demuestra que firmó el archivo. Las firmas digitales y las firmas físicas tienen el mismo valor legalmente en el Reino Unido.</p> 
              <p>La forma más simple de firma digital sería encriptar el documento con su clave privada, y luego, si alguien quisiera verificar esta firma, la desencriptaría con su clave pública y verificaría si los archivos coinciden.</p>
              <h3>Certificados - ¡Demuestre quién es usted!</h3>
              <p>Los certificados también son un uso clave de la criptografía de clave pública, vinculada a firmas digitales. Un lugar común donde se usan es HTTPS. ¿Cómo sabe su navegador web que el servidor con el que está hablando es el verdadero?</p>
              <p>La respuesta son los certificados. El servidor web tiene un certificado que dice que es el verdadero tryhackme.com. Los certificados tienen una cadena de confianza, comenzando con una CA raíz (autoridad de certificación). Su dispositivo, sistema operativo o navegador confía automáticamente en las CA raíz desde la instalación. Certificados a continuación que son confiables porque las CA raíz dicen que confían en esa organización. Certificados a continuación que son de confianza porque la CA raíz confía en la organización, etc. Hay largas cadenas de confianza. Nuevamente, esta publicación de blog explica esto mucho mejor de lo que puedo. <a href="https://robertheaton.com/2014/03/27/how-does-https-actually-work/"> https://robertheaton.com/2014/03/27/how-does-https-actually-work/</a></p>
                <p>Puede obtener sus propios certificados HTTPS para los dominios de su propiedad utilizando Let's Encrypt de forma gratuita. Si tiene un sitio web, vale la pena configurarlo.</p>
